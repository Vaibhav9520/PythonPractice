# Escape sequence
# 1= \' [For single and double  quate]
print("Hello,I\'m a boy")
print("Hello,I\"m a boy")

# 2= \\ [for backslash]
print("Hello I am a good \\ boy")

# 3=  \n [For new line]
print("This is a monkey\nThis is a Elephant")

# 4= \t [For tab space]
print("Hello Good\tMorning")

# 5= \b [For backspace]
print("Hello Good Morning\bGood Night")

